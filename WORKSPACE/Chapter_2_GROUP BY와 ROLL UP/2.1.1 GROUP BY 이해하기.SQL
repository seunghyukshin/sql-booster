-- 고객별 주문건수
SELECT CUS_ID, COUNT(1) FROM T_ORD GROUP BY CUS_ID;

-- 주문일시,지불유형별 주문금액
SELECT ORD_DT, PAY_TP, SUM(PAY_AMT) FROM T_ORD GROUP BY ORD_DT, PAY_TP;

-- 집계함수
SELECT
--     *
COUNT(*) CNT
, SUM(ORD_AMT)
, MIN(ORD_SEQ)
, MAX(ORD_SEQ)
-- , ORD_ST -- 집계함수와 같이 사용 불가
FROM T_ORD
WHERE ORD_DT >= TO_DATE('20170101', 'YYYYMMDD')
AND ORD_DT < TO_DATE('20170201', 'YYYYMMDD')
-- GROUP BY ORD_ST

;
